	<p>La rénovation énergétique offre de nombreux avantages, notamment la réduction de vos factures d'énergie, l'amélioration du confort de votre logement, l'augmentation de la valeur de votre propriété, et la réduction de votre empreinte carbone.</p>
</div>

<div class="faq-section">
	<h2>7. Quels matériaux utilisez-vous ?</h2>
	<p>Nous utilisons des matériaux de haute qualité, respectueux de l'environnement et certifiés pour leur efficacité énergétique. Nous travaillons avec des fournisseurs réputés pour garantir la durabilité et la performance de nos installations.</p>
</div>

<div class="faq-section">
