<h1>FAQ - Foire Aux Questions</h1>

<div class="faq-section">
	<h2>1. Qu'est-ce que REA ?</h2>
	<p>REA est une société spécialisée dans la rénovation énergétique. Nous proposons des services pour améliorer l'efficacité énergétique de votre maison ou de votre entreprise, réduisant ainsi vos factures d'énergie et votre impact environnemental.</p>
</div>

<div class="faq-section">
	<h2>2. Quels services proposez-vous ?</h2>
	<p>Nous offrons une gamme de services de rénovation énergétique, y compris l'isolation thermique, l'installation de systèmes de chauffage et de climatisation efficaces, la pose de panneaux solaires, et bien plus encore. Nous réalisons également des audits énergétiques pour identifier les meilleures solutions pour votre situation.</p>
</div>

<div class="faq-section">
