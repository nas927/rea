	<p>Pour obtenir un devis, vous pouvez nous contacter via notre formulaire de contact sur le site web, ou appeler notre service clientèle au +33 1 23 45 67 89. Nous organiserons une visite pour évaluer vos besoins et vous fournir un devis détaillé.</p>
</div>

<div class="faq-section">
	<h2>4. Proposez-vous des aides financières ?</h2>
	<p>Oui, nous vous informons des différentes aides financières et subventions disponibles pour la rénovation énergétique. Nous vous aidons également à monter vos dossiers pour bénéficier de ces aides.</p>
</div>

<div class="faq-section">
	<h2>5. Combien de temps durent les travaux ?</h2>
	<p>La durée des travaux dépend de l'ampleur et de la complexité du projet. Une fois que nous aurons évalué votre situation, nous vous fournirons un calendrier détaillé des travaux. En général, les petits projets peuvent être achevés en quelques jours, tandis que les projets plus importants peuvent prendre plusieurs semaines.</p>
</div>

<div class="faq-section">
