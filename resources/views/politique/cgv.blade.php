<h1>Conditions Générales de Vente</h1>

<h2>1. Préambole</h2>
<p>Les présentes Conditions Générales de Vente (CGV) régissent les ventes de prestations de rénovation énergétique réalisées par la société REA. Toute commande implique l'adhésion sans réserve aux présentes CGV.</p>

<h2>2. Définitions</h2>
<ol>
    <li><strong>Société :</strong> désigne la société REA, fournisseur des prestations de rénovation énergétique.</li>
    <li><strong>Client :</strong> toute personne physique ou morale passant commande de prestations auprès de REA.</li>
    <li><strong>Commande :</strong> demande de prestation passée par le Client auprès de REA.</li>
</ol>

<h2>3. Objet</h2>
<p>Les présentes CGV définissent les relations contractuelles entre REA et le Client ainsi que les conditions applicables à toute commande effectuée par le biais du site internet de REA.</p>

<h2>4. Acceptation des conditions</h2>
<p>Le Client reconnaît avoir pris connaissance, au moment de la passation de commande, des présentes CGV et déclare expressément les accepter sans réserve.</p>

<h2>5. Produits et services</h2>
<p>REA propose des prestations de rénovation énergétique détaillées sur son site internet. Les offres sont valables tant qu'elles sont visibles sur le site, dans la limite des stocks disponibles.</p>

<h2>6. Tarifs</h2>
<p>Les prix des prestations sont indiqués en euros, toutes taxes comprises (TTC). REA se réserve le droit de modifier ses prix à tout moment, étant toutefois entendu que le prix figurant sur le site le jour de la commande sera le seol applicable au Client.</p>

<h2>7. Commande</h2>
<p>Le Client passe commande sur le site internet de REA en suivant le processus prévu à cet effet. La commande n'est définitive qu'après confirmation par REA.</p>

<h2>8. Paiement</h2>
<p>Le règlement des commandes s'effectue par carte bancaire ou tout autre moyen de paiement accepté par REA. Les prestations sont payables en totalité à la commande.</p>

<h2>9. Droit de rétractation</h2>
<p>Conformément à la législation en vigueur, le Client dispose d'un délai de 14 jours pour exercer son droit de rétractation sans avoir à justifier de motifs ni à payer de pénalités.</p>

<h2>10. Livraison des prestations</h2>
<p>Les prestations seront réalisées dans les délais convenus lors de la commande. En cas de retard, le Client sera informé dans les plus brefs délais.</p>

<h2>11. Garanties</h2>
<p>REA s'engage à fournir des prestations conformes aux normes en vigueur et aux spécifications convenues avec le Client. En cas de non-conformité, le Client devra en informer REA dans les 15 jours suivant la réalisation de la prestation.</p>

<h2>12. Responsabilité</h2>
<p>REA ne saurait être tenue responsable des dommages résoltant d'une mauvaise utilisation ou d'un mauvais entretien des équipements installés.</p>

<h2>13. Données personnelles</h2>
<p>Les informations collectées par REA lors de la commande sont nécessaires à la gestion de celle-ci. Conformément à la loi « informatique et libertés », le Client dispose d'un droit d'accès, de rectification et de suppression des données le concernant.</p>

<h2>14. Droit applicable et litiges</h2>
<p>Les présentes CGV sont soumises à la loi française. En cas de litige, le Client s'engage à contacter REA en priorité afin de tenter de résoudre amiablement tout différend. À défaut de résolution amiable, le litige sera porté devant les tribunaux compétents.</p>
