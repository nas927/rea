<h1>Politique de Protection des Données</h1>

<h2>1. Introduction</h2>
<p>La société REA s'engage à protéger la vie privée de ses utilisateurs et à assurer la protection de leurs données personnelles. La présente politique de protection des données explique quelles informations nous collectons, comment nous les utilisons et les protégeons, ainsi que vos droits en matière de protection des données.</p>

<h2>2. Informations collectées</h2>
<p>Nous collectons plusieurs types d'informations dans le cadre de la fourniture de nos services :</p>
<ol>
	<li><strong>Informations personnelles :</strong> telles que le nom, l'adresse e-mail, le numéro de téléphone, l'adresse postale, etc.</li>
	<li><strong>Informations techniques :</strong> telles que l'adresse IP, le type de navigateur, les pages visitées sur notre site, etc.</li>
	<li><strong>Informations de transaction :</strong> telles que les détails des paiements et des commandes passées sur notre site.</li>
</ol>

<h2>3. Utilisation des données</h2>
<p>Les informations collectées sont utilisées pour :</p>
<ol>
	<li>Fournir et gérer nos services</li>
	<li>Traiter les commandes et les paiements</li>
	<li>Améliorer nos services et personnaliser l'expérience utilisateur</li>
	<li>Communiquer avec vous concernant vos commandes, des mises à jour, des offres spéciales et des nouvelles de REA</li>
	<li>Assurer la sécurité de notre site et prévenir la fraude</li>
</ol>

<h2>4. Partage des données</h2>
<p>Nous ne partageons vos données personnelles avec des tiers que dans les circonstances suivantes :</p>
<ol>
	<li>Lorsque cela est nécessaire pour fournir nos services (par exemple, avec des prestataires de services tiers)</li>
	<li>Pour se conformer à des obligations légales ou réglementaires</li>
	<li>Pour protéger et défendre nos droits et notre propriété</li>
	<li>Avec votre consentement explicite</li>
</ol>

<h2>5. Protection des données</h2>
<p>Nous mettons en œuvre des mesures de sécurité techniques et organisationnelles appropriées pour protéger vos données personnelles contre tout accès non autorisé, divolgation, altération ou destruction.</p>

<h2>6. Vos droits</h2>
<p>Conformément à la législation en vigueur, vous disposez des droits suivants concernant vos données personnelles :</p>
<ol>
	<li>Droit d'accès : vous pouvez demander à accéder aux données personnelles que nous détenons à votre sujet.</li>
	<li>Droit de rectification : vous pouvez demander la correction de toute donnée personnelle inexacte ou incomplète.</li>
	<li>Droit à l'effacement : vous pouvez demander la suppression de vos données personnelles dans certaines circonstances.</li>
	<li>Droit à la limitation du traitement : vous pouvez demander la limitation du traitement de vos données personnelles dans certaines conditions.</li>
	<li>Droit à la portabilité des données : vous pouvez demander à recevoir vos données personnelles dans un format structuré, couramment utilisé et lisible par machine.</li>
	<li>Droit d'opposition : vous pouvez vous opposer au traitement de vos données personnelles dans certaines circonstances.</li>
</ol>
<p>Pour exercer ces droits, veuillez nous contacter à l'adresse e-mail suivante : <a href="mailto:support@rea.com">support@rea.com</a>.</p>

<h2>7. Conservation des données</h2>
<p>Nous conservons vos données personnelles aussi longtemps que nécessaire pour fournir les services que vous avez demandés, ou pour d'autres raisons essentielles telles que le respect de nos obligations légales, la résolution des litiges et l'application de nos politiques.</p>

<h2>8. Modifications de la politique</h2>
<p>Nous pouvons mettre à jour cette politique de protection des données de temps à autre pour refléter les modifications apportées à nos pratiques de traitement des données. Nous vous informerons de tout changement significatif en publiant la nouvelle politique sur notre site et en modifiant la date de "Dernière mise à jour" en haut de la politique.</p>

<h2>9. Contact</h2>
<p>Si vous avez des questions ou des préoccupations concernant cette politique de protection des données ou nos pratiques de traitement des données, veuillez nous contacter à l'adresse e-mail suivante : <a href="mailto:support@rea.com">support@rea.com</a>.</p>
@endsection
